/**
 * Product Characteristics - Detail View
 * Shows characteristics for a specific product from URL parameter
 */

import { loadHeaderFooter, getApiUrl } from "./utils.mjs";

loadHeaderFooter();

const API_URL = "/product-char";
let currentProductId = null;
let allCharacteristics = [];

/**
 * Get product ID from URL query parameter
 */
function getProductIdFromUrl() {
  const params = new URLSearchParams(window.location.search);
  return params.get("productId");
}

/**
 * Initialize - load characteristics for the product from URL
 */
export async function initializeCharacteristics() {
  const productId = getProductIdFromUrl();

  if (!productId) {
    document.getElementById("listContainer").innerHTML =
      '<div class="empty-message">No product selected. <a href="products.html">← Back to Products</a></div>';
    return;
  }

  currentProductId = productId;
  document.getElementById("pageTitle").textContent =
    `Product Characteristics - ${productId}`;

  try {
    const response = await fetch(`${API_URL}/${encodeURIComponent(productId)}`);
    allCharacteristics = await response.json();

    if (!allCharacteristics || allCharacteristics.length === 0) {
      document.getElementById("listContainer").innerHTML =
        '<div class="empty-message">No characteristics found for this product. Click "+ Add" to create one.</div>';
      return;
    }

    renderTable(allCharacteristics);
  } catch (error) {
    document.getElementById("listContainer").innerHTML =
      `<div class="empty-message">Error loading characteristics</div>`;
  }
}

/**
 * Open the add characteristic modal
 */
export async function openModal() {
  const modal = document.getElementById("modal");
  const backdrop = document.getElementById("backdrop");
  const form = document.getElementById("form");
  const errorBox = document.getElementById("errorBox");

  // Clear form and error
  form.reset();
  errorBox.classList.remove("show");
  backdrop.classList.add("open");
  modal.classList.add("open");

  // Pre-fill with current product ID
  document.getElementById("PRODUCT_ID").value = currentProductId;
  document.getElementById("REVISION_LEVEL").focus();
}

/**
 * Close the modal
 */
export function closeModal() {
  const modal = document.getElementById("modal");
  const backdrop = document.getElementById("backdrop");
  const errorBox = document.getElementById("errorBox");

  backdrop.classList.remove("open");
  modal.classList.remove("open");
  errorBox.classList.remove("show");
  document.getElementById("form").reset();
}

/**
 * Save characteristic to database
 */
export async function saveCharacteristic() {
  const form = document.getElementById("form");
  const errorBox = document.getElementById("errorBox");

  errorBox.classList.remove("show");
  errorBox.textContent = "";

  // Get form values
  const productId = (document.getElementById("PRODUCT_ID").value || "")
    .trim()
    .toUpperCase();
  const revisionLevel = (
    document.getElementById("REVISION_LEVEL").value || ""
  ).trim();

  // Validate
  if (!productId) {
    showError("Product ID is required");
    return;
  }
  if (!revisionLevel) {
    showError("Revision Level is required");
    return;
  }

  // Collect form data - CHAR_NO is auto-generated by backend
  const data = {
    PRODUCT_ID: productId,
    REVISION_LEVEL: revisionLevel,
    CREATE_BY: getCurrentUser(),
  };

  // Add optional fields
  const fields = [
    "NAME",
    "TYPE",
    "DRAWING_NO",
    "INSP_PLAN_EQUIP_ID",
    "ISSUE_DATE",
    "STANDARD_TYPE",
    "VARIABLE_STANDARD",
    "UNITS",
    "PAGE",
    "ZONE",
    "STATUS",
    "CATEGORY",
    "CLASS",
    "INSP_PLAN_EQP_TYPE",
    "INSP_PLN_SAMP_PLAN",
    "INSP_PLAN_AQL",
    "INSP_PLAN_LEVEL",
    "INSP_PLAN_DEV_ID",
    "INSP_PLAN_DEV_TYPE",
    "INSP_PLAN_CHT_TYPE",
    "INSP_PLAN_MEAS_BY",
    "INSP_PLAN_FREQ",
    "INSP_PLAN_CHART_BY",
    "SIGNIFICAT_DIGITS",
    "SAMP_PLAN_ID",
  ];

  for (const field of fields) {
    const el = document.getElementById(field);
    if (el) {
      const value = el.value.trim();
      data[field] = value || null;
    }
  }

  // Also handle numeric fields
  const numFields = ["NOMINAL", "LOWER", "UPPER", "INSP_PLN_SAMP_SIZE"];
  for (const field of numFields) {
    const el = document.getElementById(field);
    if (el) {
      const value = el.value.trim();
      data[field] = value ? parseFloat(value) : null;
    }
  }

  try {
    const response = await fetch(API_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(data),
    });

    const result = await response.json();

    if (response.status === 202 && result.requiresConfirmation) {
      const proceed = confirm(`${result.warning}\n\nProceed anyway?`);
      if (!proceed) return;

      data.confirmWarning = true;
      const retryResponse = await fetch(API_URL, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(data),
      });

      if (!retryResponse.ok) {
        const error = await retryResponse.json();
        showError(error.error || "Failed to save");
        return;
      }

      // Retry succeeded - close modal and refresh list
      closeModal();
      await initializeCharacteristics();
      return;
    } else if (!response.ok) {
      const error = await response.json();
      showError(error.error || "Failed to save");
      return;
    }

    // Success - close modal and refresh list
    closeModal();
    await initializeCharacteristics();
  } catch (error) {
    showError(`Error: ${error.message}`);
  }
}

/**
 * Render characteristics table
 */
function renderTable(characteristics) {
  const container = document.getElementById("listContainer");

  const html = `
    <table>
      <thead>
        <tr>
          <th>Char No</th>
          <th>Name</th>
          <th>Type</th>
          <th>Nominal</th>
          <th>Lower</th>
          <th>Upper</th>
          <th>Units</th>
          <th>Revision</th>
          <th>Status</th>
          <th>Drawing No</th>
        </tr>
      </thead>
      <tbody>
        ${characteristics
          .map(
            (c) => `
          <tr>
            <td><span class="badge badge-char">${c.CHAR_NO}</span></td>
            <td>${c.NAME || "-"}</td>
            <td>${c.TYPE || "-"}</td>
            <td>${formatNumber(c.NOMINAL)}</td>
            <td>${formatNumber(c.LOWER)}</td>
            <td>${formatNumber(c.UPPER)}</td>
            <td>${c.UNITS || "-"}</td>
            <td>${c.REVISION_LEVEL || "-"}</td>
            <td>${
              c.STATUS === "A"
                ? '<span class="badge badge-ok">Active</span>'
                : c.STATUS === "I"
                  ? '<span class="badge badge-no">Inactive</span>'
                  : "-"
            }</td>
            <td>${c.DRAWING_NO || "-"}</td>
          </tr>
        `,
          )
          .join("")}
      </tbody>
    </table>
  `;

  container.innerHTML = html;
}

/**
 * Helper: Format numbers
 */
function formatNumber(val) {
  if (!val) return "-";
  if (typeof val === "number") {
    return val.toFixed(6).replace(/0+$/, "").replace(/\.$/, "");
  }
  return val;
}

/**
 * Helper: Show error
 */
function showError(msg) {
  const errorBox = document.getElementById("errorBox");
  errorBox.textContent = msg;
  errorBox.classList.add("show");
}

/**
 * Helper: Get current user
 */
function getCurrentUser() {
  return localStorage.getItem("currentUser") || "SYSTEM";
}

/**
 * Refresh the list
 */
export async function refreshList() {
  await initializeCharacteristics();
}
